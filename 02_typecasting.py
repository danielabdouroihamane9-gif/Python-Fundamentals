#3 type casting: the process of converting a variable from one data type to another
#                 str(), int(), float(), bool()
name = "Daniel"
age = 20
gpa = 3.2
is_student = False
print(type(gpa))
print(type(is_student))
print(type(age))
print(type(name))

gpa = int(gpa)
print(gpa)
print(int(is_student))
